#include "3-calc.h"

/**
 * op_add - function to add two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to subtract two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function to multiply two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function to carry out division between two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function to carry out division between two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
